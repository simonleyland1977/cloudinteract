import { NextResponse } from 'next/server';
import type { NextRequest } from 'next/server';

export function middleware(request: NextRequest) {
    const { pathname } = request.nextUrl;

    // Protect AWS Portal routes
    if (pathname.startsWith('/aws-portal')) {
        // Allow access to the main portal page (login/signup)
        if (pathname === '/aws-portal') {
            return NextResponse.next();
        }

        // For protected portal routes, authentication will be handled client-side
        // via the ProtectedRoute component to avoid middleware complexity with Amplify auth
        return NextResponse.next();
    }

    return NextResponse.next();
}

export const config = {
    matcher: [
        /*
         * Match all request paths except for the ones starting with:
         * - api (API routes)
         * - _next/static (static files)
         * - _next/image (image optimization files)
         * - favicon.ico (favicon file)
         * - public assets
         */
        '/((?!api|_next/static|_next/image|favicon.ico|.*\\.(?:svg|png|jpg|jpeg|gif|webp)$).*)',
    ],
};
